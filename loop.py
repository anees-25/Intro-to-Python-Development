# Initialize a list of people
people = ['Anees', 'Awais']

# Initialize an index variable to 0
index = 0

# Use a while loop to iterate over the elements in the 'people' list
while index < len(people):
    # Print the current element from the 'people' list using the index
    print(people[index])
    
    # Increment the index by 1
    index = index + 1